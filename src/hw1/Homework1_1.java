package hw1;

public class Homework1_1 {

//題目1：請設計一隻Java程式,計算12,6這兩個數值的和與積
	
	public static void main (String[] args) {
        // 定義兩個數值
        int num1 = 12;
        int num2 = 6;
        
        // 計算和與積
        int sum = num1 + num2;
        int product = num1 * num2;
        
        // 輸出結果
        System.out.println("和: " + sum);
        System.out.println("積: " + product);
     }
}
